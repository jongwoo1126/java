package Ch04;

public class p53 {

	public static void main(String[] args) {
		
		int num1 =10;
		System.out.println("num1 = 10 -> "+num1);
		num1 += 10;
		System.out.println("num1 += 10 -> "+num1);
		num1 -= 10;
		System.out.println("num1 -= 10 -> "+num1);
		num1 *= 2;
		System.out.println("num1 *= 2 -> "+num1);
		num1 /= 2;
		System.out.println("num1 /= 2 -> "+num1);
		num1 %= 3;
		System.out.println("num1 %= 3 -> "+num1);
	}
}
